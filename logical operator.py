y=20
print(x==10 and x<y)  #and   if all condition is true then result true
print(x==10 and x<y and x==y)  #and if 1  condition is false then result false

print(x!=10 or x<y ) #if 1  condition is ture then result true
print(x!=10 or x<y or x==y)#if 1  condition is ture then result true

print( x!=10)
